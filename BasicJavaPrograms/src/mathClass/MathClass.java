package mathClass;

public class MathClass {

    public static void main(String[] args) {
        System.out.println("e="+Math.E);
        System.out.println("Pi="+Math.PI);
        System.out.println("Square Root of 64 = "+Math.sqrt(64));
        System.out.println("Cube Root of 64 = "+Math.cbrt(64));
        System.out.println("sin(30)="+Math.sin(30*Math.PI/180));
        System.out.println("cos(30)="+Math.cos(30*Math.PI/180));
        System.out.println("tan(30)="+Math.tan(30*Math.PI/180));
        System.out.println("ln(10)="+Math.log(10));
        System.out.println("log_10(1000)="+Math.log10(1000));
        System.out.println("24^6="+Math.pow(24,6));
        System.out.println("Random number between 0.0 to 1.0 = "+Math.random());
    }

}
